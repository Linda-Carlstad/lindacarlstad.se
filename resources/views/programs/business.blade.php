    <h1>IT-design: Affärssystem och ekonomi</h1>
    <hr>
    <p>
        Utbildningen i IT-design förbereder dig för arbetsuppgifter inom allt från anpassning och
        upphandling av affärssystem till avancerad programvaruutveckling på en växande arbetsmarknad.
        I stort sett alla företag använder idag någon form av IT-system, oavsett bransch. För att dessa,
        ofta komplexa, system ska kunna kravställas, utvecklas, användas och integreras behövs du
        som IT-designer. Utbildningen i IT-design ger dig möjlighet att bedriva dina studier under
        flexibla förhållanden. Du läser gemensamma kurser den första studietiden för att därefter
        fördjupa dig inom en av inriktningarna Affärssystem och ekonomi, Programvarudesign eller
        Systemdesign. IT-design är en utbildning som ger dig goda möjligheter till samverkan med det
        lokala näringslivet och möjligheter att bedriva delar av dina studier utomlands. Målet med
        programmet är att ge dig en plattform och möjligheter till användbara kunskaper inom IT som
        grund för ett stimulerande och utvecklande jobb. En förberedelse inför en framtid i IT-branschen.
    </p>
    <p>
        Affärssystem och ekonomi är en inriktning för dig som är intresserad av IT och
        företagsekonomi med fokus på affärssystem. Inom programmet läser du IT i kombination
        med företagsekonomi, logistik, verksamhets- och tjänsteutveckling. Du lär dig modeller
        för analys och kravspecificering. Du kommer att få lösa praktiska uppgifter inom
        affärssystemområdet med nära koppling till näringslivet. Målet med programmet är
        att ge dig en plattform och möjligheter till användbara kunskaper inom IT som grund
        för ett stimulerande och utvecklande jobb. IT-design ger dig möjlighet att bedriva
        dina studier flexibelt med undervisning både på campus och med stöd av vår webbaserade
        utbildningsplattform som ett komplement.
    </p>

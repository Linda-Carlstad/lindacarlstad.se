        <h2>Föreningen</h2>
        <p>
            LINDA är en studentförening under Karlstad Universitet.
            LINDA bevakar ditt intresse som student, såväl att du har det bra på universitet som på fritiden,
            utbildningen kommer inte alltid att vara rolig eller lätt och då hoppas vi att vi kan vara till din hjälp med studierna om det så krävs eller med att bara ta det lugnt och umgås.
            Bland annat brukar många av oss gå till <a class="link link-underline" href="http://www.studentpuben-bunkern.se/">studentpuben Bunkern</a> där man kan gå och ta en öl/läsk/kopp kaffe eller äta god mat och spela spel och ta det lugnt mellan varven på skolan.
            LINDA grundades 1984.
        </p>

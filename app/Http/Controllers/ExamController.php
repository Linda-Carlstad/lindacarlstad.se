    {
        if ($request->hasFile('file')) {


            $filename = $request->file->getClientOriginalName();
            $request->file->storeAs('public/upload',$filename);

            return redirect()->back()->with('status', 'Tenta upplagd!');
        }

        return $request->all();
    }

